
public class IncrementDecrement {

	public static void main(String[] args) {
		int a=9;
		System.out.println(a);
		++a;
		System.out.println("++a="+ ++a);
		a++;
		System.out.println("a++="+ a++);
		--a;
		System.out.println("--a="+ --a);
		a--;		
		System.out.println("a--="+ a--);

	}

}
